package com.mycompany.models;

public class Packs {
    private int id;
    private String name;
    private int cant_class;
    private int price;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getCant_class() {
        return cant_class;
    }

    public void setCant_class(int cant_class) {
        this.cant_class = cant_class;
    }

    public int getPrice() {
        return price;
    }

    public void setPrice(int price) {
        this.price = price;
    }
    
}
