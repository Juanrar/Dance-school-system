    private void BottonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BottonActionPerformed
        int packId = comboBox.getSelectedIndex()+2;
        try{
            DAOPays daopays = new DAOPaysImp();
            DAOPacks daopacks = new DAOPacksImp();
            Packs pack = daopacks.selectPack(packId);
  
            Pays pay = new Pays();
            pay.setStudent(student);
            pay.setPack(pack);
            daopays.register(pay);
            
            javax.swing.JOptionPane.showMessageDialog(this,"Pago Exitoso","AVISO", javax.swing.JOptionPane.INFORMATION_MESSAGE);
        } catch(Exception e) {
            System.out.println(e.getMessage());
            javax.swing.JOptionPane.showMessageDialog(this,"Ocurrio un error al crear pago","AVISO", javax.swing.JOptionPane.ERROR_MESSAGE);
        }
        dispose();
    }//GEN-LAST:event_BottonActionPerformed
    
    
