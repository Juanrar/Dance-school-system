    private void loadPacks(){
        try {
           comboBox.removeAllItems();
           DAOPacks dao = new DAOPacksImp();
           dao.list().forEach((u)-> comboBox.addItem(u.getName()));
       } catch (Exception e) {
           System.out.println("Error cargando packs: " + e.getMessage());
       }
    }
    
    

